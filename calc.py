class Calculadora:

    def __init__(self) -> None:
        pass

    def soma(self, num1: float, num2: float):
        return num1+num2

    def subtrai(self, num1: float, num2: float):
        return num1-num2

    def divide(self, num1: float, num2: float):
        return num1/num2

    def multiplica(self, num1: float, num2: float):
        return num1*num2
